package json;

public class BuyShare {
    String date;
    double shares;
    String ticker;

    public String getDate() {
        return date;
    }

    public double getShares() {
        return shares;
    }

    public String getTicker() {
        return ticker;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public void setShares(double shares) {
        this.shares = shares;
    }

    public void setTicker(String ticker) {
        this.ticker = ticker;
    }


}
